/// European Pay spacing system.
/// Consistent 4px base spacing scale for uniform visual rhythm.
abstract final class AppSpacing {
  /// 0px
  static const double zero = 0;

  /// 2px - Hairline spacing
  static const double xxs = 2;

  /// 4px - Minimal spacing
  static const double xs = 4;

  /// 8px - Tight spacing
  static const double sm = 8;

  /// 12px - Compact spacing
  static const double md = 12;

  /// 16px - Default spacing
  static const double lg = 16;

  /// 20px - Comfortable spacing
  static const double xl = 20;

  /// 24px - Generous spacing
  static const double xxl = 24;

  /// 32px - Section spacing
  static const double xxxl = 32;

  /// 40px - Large section spacing
  static const double section = 40;

  /// 48px - Component spacing
  static const double component = 48;

  /// 56px - Large component spacing
  static const double componentLg = 56;

  /// 64px - Page spacing
  static const double page = 64;

  /// 80px - Hero spacing
  static const double hero = 80;

  // ── Radius ─────────────────────────────────────────────────────
  /// 4px radius
  static const double radiusXs = 4;

  /// 6px radius
  static const double radiusSm = 6;

  /// 12px radius
  static const double radiusMd = 12;

  /// 16px radius
  static const double radiusLg = 16;

  /// 20px radius
  static const double radiusXl = 20;

  /// 24px radius
  static const double radiusXxl = 24;

  /// 999px radius - Full/pill radius
  static const double radiusFull = 999;

  // ── Sizing ─────────────────────────────────────────────────────
  /// 32px - Small icon button
  static const double iconButtonSm = 32;

  /// 40px - Medium icon button
  static const double iconButtonMd = 40;

  /// 48px - Default touch target
  static const double touchTarget = 48;

  /// 56px - Large button / input height
  static const double buttonHeight = 56;

  /// 44px - Compact button height
  static const double buttonHeightSm = 44;

  /// 64px - Bottom nav height
  static const double bottomNavHeight = 64;

  /// 48px - App bar leading
  static const double appBarLeading = 48;

  /// 40px - Avatar small
  static const double avatarSm = 40;

  /// 48px - Avatar medium
  static const double avatarMd = 48;

  /// 56px - Avatar large
  static const double avatarLg = 56;

  /// 80px - Avatar extra large
  static const double avatarXl = 80;

  // ── Page padding ───────────────────────────────────────────────
  /// 16px - Horizontal page padding
  static const double pagePaddingH = 16;

  /// 20px - Horizontal page padding (wide)
  static const double pagePaddingHWide = 20;

  /// 24px - Vertical page padding
  static const double pagePaddingV = 24;
}
